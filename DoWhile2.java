import java.util.*;
class DoWhile2 {
    public static void main(String args[]) {
        int i=0;
        while(i<10){
			System.out.println("while-loop : "+i);
			i++;
		}
		//---------------------------------------
		System.out.println("--------------------");
		int j=0;
		do{
			System.out.println("do/while-loop : "+j);
			j++;
		}while(j<10);
			
    }
}
