import java.util.*;
class Exercise7 {
    public static void main(String args[]) {
		Scanner input=new Scanner(System.in);
		System.out.print("Input number- ");
		int n=input.nextInt();
		
		if(n==0){
			System.out.println("[Empty]");
		}
		
		else{
		System.out.print("[");
		for (int i = 0; i < n; i++)
		{
			System.out.print(n);
		}
		 
		System.out.println("]");
		
	}
		
		
		
		
		
		
}

}

